export function t(lang: Language, key: keyof typeof translations.ja, replacements?: Record<string, string | number>) {
  let text = translations[lang][key] || translations.ja[key] || key;
  if (replacements) {
    Object.keys(replacements).forEach(k => {
      text = text.replace(`{${k}}`, String(replacements[k]));
    });
  }
  return text;
}
